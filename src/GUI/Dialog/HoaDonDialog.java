    private void setupBanAnComboBox() {
        try {
            Connection conn = DBConnector.getConnection();
            banAnBUS = new BanAnBUS(conn);
            List<BanAnDTO> availableTables = banAnBUS.AvailableTable();
            
            jComboBoxBanAn.removeAllItems();
            for (BanAnDTO ban : availableTables) {
                jComboBoxBanAn.addItem(ban);
            }
            
            // Override toString to show table name
            jComboBoxBanAn.setRenderer(new javax.swing.DefaultListCellRenderer() {
                @Override
                public java.awt.Component getListCellRendererComponent(
                    javax.swing.JList<?> list, Object value, int index,
                    boolean isSelected, boolean cellHasFocus) {
                    super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                    if (value instanceof BanAnDTO) {
                        setText(((BanAnDTO) value).getTen());
                    }
                    return this;
                }
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải danh sách bàn: " + e.getMessage());
        }
    }

