    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            if (jComboBoxBanAn.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn bàn", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Connection conn = DBConnector.getConnection();
            HoaDonDTO hdDTO = new HoaDonDTO();
            hdDTO.setTenKhach(jTextFieldTenKH.getText().trim());
            hdDTO.setIdNhanVien(Integer.parseInt(jTextFieldIDNV.getText().trim()));
            hdDTO.setTongTien(Integer.parseInt(jTextFieldTongTien.getText().trim()));
            hdDTO.setIdBanAn(((BanAnDTO)jComboBoxBanAn.getSelectedItem()).getId());
            hdDTO.setThoiGian(new Timestamp(new Date().getTime()));
            hdDTO.setGhiChu(jTextFieldGhiChu.getText().trim());

            HoaDonBUS hdBUS = new HoaDonBUS(conn);
            Result result = hdBUS.add(hdDTO);
            
            if (result.isSuccess()) {
                JOptionPane.showMessageDialog(this, result.getMessage());
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, result.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ID nhân viên và tổng tiền là số", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

